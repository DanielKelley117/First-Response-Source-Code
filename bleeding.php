    <h1 data-splitting>Step 1: Treating Severe Bleeding.</h1>
    <p>When severe bleeding occurs, it is important to remember the abbreviates P.E.T. This abbreviation stands for pressure, elevation, and tourniquet. These three things
    are very helpful in stopping severe bleeding.</p>
      <p>First, make sure you remove all clothing or anything else that may be covering the wound. This is important in treating the bleeding, and preventing an infection. Next, place
      gauze or a sterile bandage over the wound and place constant pressure on the bandage. The pressure is important in making sure the bleeding stops. Secure the bandage in place with
      adhesive tape, but don't stop putting pressure on the wound.</p>
    <p>Have the victim lie down, preferrably in a space where they won't lose body heat. If possible, have the victim elevate the wound above the heart to reduce the blood loss.</p>
    <p>Utilize a tourniquet if necessary, and immobolize the wounded body part. Call 911 if the bleeding does not stop.</p>
