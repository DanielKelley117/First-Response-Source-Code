    <h1 data-splitting>Steps</h1>
    <p>Signs of choking include gasping, wheezing, coughing, panic, and clutching of the throat.</p>
      <p>If the victim is not an infant, begin with 5 backblows between the shoulder blades, followed by 5 abdomonal thrusts. Perform an abdomonal thrust by standing behind the victim
      and wraping your arms around them. Clench one of your hands into a fist, and grab the fist with your other hand. Place your hands directly above the victim's navel and pull inward and upward.</p>
      <img src="img/Chokingimage1.jpg", alt="Chokingimage1", width="700", height="600">
    <p>Alternate between 5 back blows and 5 abdomonal thrusts. If the victim is obese or pregnant place your arms at the breast bone when doing abdomonal thrusts. </p>
    <p>If the victim is an infant, be gentle with your backblows, and use 2 fingers to give chest compressions instead of abdomonal thrusts.</p>
    <p> If the victim becomes unconscious, lower them to the ground, open their airway, and begin CPR starting with chest compressions. </p>
