    <h1 data-splitting>Allergic Reaction</h1>
    <p>Breathing problems could be caused by Allergic Reactions. If this is the case, call 911 immediately and use an epipen.</p>
    <img src="img/Epipenimage1.jpg", alt="Epipenimage1", width="500", height="500">
      <p>To use an epipen, take the cap off the top and stab the victim in the thigh with the other end of the epipen. Do not worry about clothing, as the pen will go right through any article of clothing.</p>
    <p>Usually people with allergies carry their own epipens, but if the victim does not have one, you could use an epipen from someone else who has the same allergy. However, it is best
    to do this only as a last resort.</p>
    <p>Be prepared to perform CPR if the victim is unconscious.</p>
