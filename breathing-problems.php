    <h1 data-splitting>Asthma attack</h1>
    <p>Signs of an asthma attack include coughing, wheezing, and chest tightness.</p>
    <p>To assist in an asthma attack, use the an inhaler. First take the cap off the inhaler, and place the mouthpiece in the victim's mouth.
    Next, have the victim breath in slowly, and press down on the inhaler. After taking the inhaler out of the victim's mouth, ask the victim to hold their breath for
    10 seconds if they are capable of doing so. Call 911 if the victim's condition does not improve.</p>
    <img src="img/Inhalerimage1.jpg", alt="Inhalerimage1", width="500", height="500">
    <p>If the victim does not have an inhaler, you can use one from someone else, but it is better to do so as a last resort.</p>
