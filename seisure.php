    <h1 data-splitting>Seisure treatment</h1>
    <p>A seisure occurs when there is an unnatural electrical overload in the brain. It usually causes the victim to start shaking violently and involuntarily. Seisures are most common in
    people with epilepsy.</p>
      <p>If you see someone experiencing a seisure, the most important thing to do is make sure they are safe. Remove anything harmful from the area until the seisure ends. Most seisures end in a few minutes.
      Do not try to hold the victim down and stop them from moving, as that could hurt them more. Make sure you call 911.</p>
    <p>After the seisure ends, comfort the victim and ask them if they have a history of seisures. Make sure they are safe and feeling okay.</p>
    <img src="img/Seisureimage1.jpg", alt="Seisureimage1", width="500", height="500">
    <p>Help the victim get to a hospital if 911 was not already called. If they are feeling okay, they can possibly see their primary care physician instead of going to the hospital.</p>
