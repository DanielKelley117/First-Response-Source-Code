    <h1 data-splitting>Step 2: Treating a stroke</h1>
    <p>If you see someone experiencing a stroke, call 911 at once, and wait for an ambulance. Do not attempt to drive the victim to the hospital yourself
    as the victim must be treated by the EMS personnel in the ambulance vehicle to ensure that they are treated as quickly as possible.</p>
    <p>While waiting for EMS personnel to arrive, make the victim as comfortable as you can. Have them sit in a chair and monitor their condition. Do not give them
    anything to eat or drink, and do not give them any medications. Just comfort the victim, and make sure they do not leave.</p>
