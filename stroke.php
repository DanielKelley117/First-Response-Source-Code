    <h1 data-splitting>Step 1: Identifying a stroke</h1>
    <p>A stroke occurs when blood flow to the brain is diminished, causing cell death. Strokes are most common in the elderly, and people with certain conditions,
    typically heart conditions.</p>
    <img src="img/Stokeimage1.jpg", alt="Stokeimage1", width="300", height="300">
      <p>When it comes to responding to a stroke, every second is critical. The victim can usually make a full recovery if their stroke is identified and
      treated within an hour. In order to make sure the victim gets treated by medical personnel immediately, it is important to know the signs and symptoms of a stroke.</p>

    <p>Use the abbreviation F.A.S.T. This abbreviation stands for face, arms, speech, and time. If you see someone who you suspect has a stroke, pay close attention
    to their face. If they are having a stroke, half of their face will look droopy, and that will be most noticeable when the victim smiles. Also pay attention to
    their arms. Ask them to place both of their arms in front of them. If they are having a stroke, they will have trouble keeping one of their arms up. Next, ask
    them to speak. If they are having a stroke, their speech will be slurred and strange. Lastly, pay close attention to the time. Find out what time it was when the
    victim started experiencing symptoms. This information will be very helpful for the EMS personnel.</p>
    <p>If you see someone experiencing any of these symptoms, call 911 immediately.</p>
