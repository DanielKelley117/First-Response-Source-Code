    <h1 data-splitting>Step 1: Primary Assessment</h1>
    <p>If you come across someone who appears unconscious, the first thing you will need to do is to perform a primary assessment. It is also important that you alert other
    people to what is going on if you are in a public place.</p>
      <p>First you need to make sure the scene is safe. Move the victim to a new location if the location you are currently in is hazardous. Next you need to check for
      responsiveness. Tap the victim on the shoulders and ask if they are okay. If they respond, try to ask them what happened and call for medical help.</p>
    <p>If the victim is not responsive, check to make sure they are breathing. To do so, place your ear to their mouth and nose with your head facing their stomach.
    While in this position, check to see if you can feel their breath in your hear, and if you can see their chest rise. If the victim is not breathing, check for a pulse.</p>
    <img src="img/Checkbreathingimage2.jpg" alt="Checkbreathingimage" width="500" height="500">
    <p>To check for a pulse, place your index and middle finger on the victim's neck directly below their head. You will feel their heart beating if they have a pulse.
    If the victim is breathing and has a pulse then call for medical help and monitor the victim's condition. If the victim is not breathing, but has a pulse
    then begin performing rescue breaths. If the victim is not breathing and does not have a pulse, begin CPR. Instructions for the processes mentioned here are below.</p>
    <img src="img/CheckingPulseimage1.jpg" alt="CheckingPulseimage" width="500" height="500">
