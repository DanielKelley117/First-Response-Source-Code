    <h1 data-splitting>Step 2: Perofrming Rescue Breaths</h1>
    <p>Before performing rescue breaths, make sure 911 is called. Instruct bystanders to call 911, or call 911 yourself if you are alone. After you ensure that ems
    personnel are on their way, begin performing rescue breaths.</p>
    <img src="img/Rescuebreathimage3.jpg", alt="Rescuebreathimage3", width="500", height="500">
    <p>To perform rescue breaths, tilt the victims head back to open their airway. Place your mouth over the victim's and make a tight seal. Take a deep
    breath and breath into the victim's mouth. Make sure you see the victim's chest rise so that you know you're giving the victim enough air. If the victim
    has become unconscious due to choking, you may need to examine their airway between breaths to check to see if you can clear the airway of the obstruction.</p>
