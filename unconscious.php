      <h1 data-splitting>Step 3: Perofrming CPR</h1>
      <p>Before performing CPR make sure that 911 has been called. Instruct bystanders to call 911, or call 911 yourself if you are alone. If possible, tell bystanders to
      get an AED as well.Also make sure you clear the victim's chest of all clothing before beginning CPR.</p>
      <img src="img/CPRimage1.jpg", alt="CPRimage1", width="500", height="500">
      <p>If the person you are performing CPR on is an infant, then scroll down, as there are special instructions in that case.
      To perform CPR open your left hand and spread your fingers apart, and grab the back of your left hand with your right hand. Place your hands next to the Victim's
      heart, and push down 2 to 2.4 inches for an adult, and 2 inches for a child. This is called a chest compression. Perform 30 chest compressions at a rate of two
      chest compressions per second. This is slightly faster than the rythm of the Bee Gee's 'Stayin Alive.' Afterwards, tilt their head back to open their airway, and place your mouth
      over theirs, creating a tight seal. Give the victim two deep breaths, 6 seconds each. Make sure that the victim's chest rises so that you know they are getting enough air.</p>
      <p>After giving two breaths, give 30 more chest compressions, and continue a cyle of 30 chest compressions and 2 breaths. If you are with other people,
      have them help you perform CPR. Have one of you give breaths, and the other perform chest compressions, and switch off when each of you get tired.</p>
    </section>
