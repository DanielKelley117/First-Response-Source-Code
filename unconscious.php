          <h1 data-splitting>Step 4: Perofrming CPR on an infant, and AEDs</h1>
          <p>Performing CPR on an infant has an important difference. You must use your index and middle finger for chest compressions.</p>
          <img src="img/AEDimage1.jpg", alt="AEDimage1", width="500", height="500">
          <p>An AED is an Automated External Defibrilator, and it is helpful in cases when a victim has no pulse. AEDs usually have instructions. Attach the AED pads to the victim's bare chest.
          Make sure the pads are not touching each other, or touching anything metal the victim may have on them. If the victim is an infant, you may need to put one pad on the front, and one on the back.
          Follow the instructions of the AED after turning it on, and make sure everyone stands clear of the victim when the AED is about to deliver a shock.</p>
