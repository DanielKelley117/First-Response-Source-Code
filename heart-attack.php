    <h1 data-splitting>Step 2: Treating a Heart Attack</h1>
    <p>After calling 911, comfort the victim and monitor their condition while waiting for EMS personnel. Give them chew and swallow aspirin, as it may help ot reduce
    blood clotting. Ask them about any possible previous heart problems, and have them take medicine if they have been prescribed any.</p>
    <img src="img/HeartAttackimage1.jpg", alt="HeartAttackimage1", width="500", height="500">
    <p>Know that you may have to perform CPR or use an AED if the victim becomes unconscious.</p>
