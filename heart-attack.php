    <h1 data-splitting>Step 1: Identifying a Heart Attack</h1>
    <p>A heart attack occurs when a heart muscle does not get enough blood. It is most common in the elderly, the obese, people with heart conditions, and people
    suffering severe stress.</p>
    <img src="img/HeartAttackimage3.jpg", alt="HeartAttackimage3", width="500", height="500">
      <p>Heart attacks can sometimes be tricky to spot because they can vary in their severity. It is entirely possible, and not even that uncommon for a heart attack
      victim to be conscious and calm during the early stages of their heart attack. However, that does not make the heart attack itself any less harmful to them, so it is important to act quickly.</p>
    <p>The most common signs of a heart attack are the feeling of pressure on the chest, shortness of breath, feeling lightheaded, pain or discomfort in the arms or shoulders
    (or sometimes the stomach in women), and profuse sweating.</p>
    <img src="img/HeartAttackimage2.jpg", alt="HeartAttackimage2", width="500", height="500">
    <p>If you see someone experiencing any of these symptoms call 911 at once, even if the victim is conscious and feels okay.
   It is better to be safe than sorry.</p>
